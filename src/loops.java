
public class loops {

	public static void main(String[] args) {
		
		while(true) { // while(this condition is met) run everything inside || You can also use an iterator as a condition
			//will be your code that will run as long as the condition above is met
		}
		
		
		for (int i = 0; i < 5; i++) { //for (loop starter, condition needed to exit for loop, every time loop is ran run this code)
			//Every time the condition above is satisfied, the code in here will run 
		}
		
		for (String string : args) { //for (each item in a set of items)
			//Every time there is an item in the set of items, the code in here will run
		}
	}
}
